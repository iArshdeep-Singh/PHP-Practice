<?php
// The PHP 'date()' function is used to format a date and/or a time.

// Get a date
echo "Today is " . date("Y/m/d") . "\n";
echo "Today is " . date("Y.m.d") . "\n";
echo "Today is " . date("Y-m-d") . "\n";
echo "Today is " . date("y-m-d") . "\n";
echo "Today is " . date("l") . "\n";

// Get a time
echo "The time is " . date("h:i:sa") . "\n";
echo "The time is " . date("H:i:s") . "\n";
?>